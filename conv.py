def sort(goods):
    name = str(goods[1].get('name'))
    goods.pop(1) # —É–¥–∞–ª—è–µ–º –∫–ª—é—á —Å –Ω–∞–∑–≤–∞–Ω–∏–µ–º —Ç–æ–≤–∞—Ä–∞
    for i in range(len(goods)):
        for j in range(len(goods)-i-1):
            if goods[j].get('rub') < goods[j+1].get('rub'):
                goods[j], goods[j+1] = goods[j+1], goods[j]
    return goods, name

