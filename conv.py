def get_cours(headers, session):
    url = 'https://pokur.su/gbp/'
    request = session.get(url, headers=headers) # –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–ø—Ä–æ—Å
    soup = bs(request.content, 'lxml') # –ø–æ–ª—É—á–∞–µ–º HTML –∫–æ–¥
    cours = []
    valuet = ['rub', 'aud', 'twd', 'hkd', 'ils', 'cny', 'eur', 'sek', 'chf']
    for i in range(len(valuet)):
        link = ''.join(['/gbp/', valuet[i], '/1/']) # –∫–æ–Ω—Å—Ç—Ä—É–∏—Ä—É–µ–º —Å—Å—ã–ª–∫—É
        a = soup.find('a', attrs={'href': link}).text # –Ω–∞—Ö–æ–¥–∏–º –∫—É—Ä—Å —Ç–µ–∫—É—â–µ–π –≤–∞–ª—é—Ç—ã
        if ',' in a:
            a = float(a.replace(',', '.')) # —Ä–µ–¥–∞–∫—Ç–∏—Ä—É–µ–º –∏ –ø–µ—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –∑–Ω–∞—á–µ–Ω–∏–µ –∫—É—Ä—Å–∞
        cours.append({
            valuet[i].upper(): a
        })
    return cours
